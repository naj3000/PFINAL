    private void tblmundialMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblmundialMouseClicked
        // TODO add your handling code here:
        try {
            int fila = tblmundial.getSelectedRow();
            int Id= Integer.parseInt(tblmundial.getValueAt(fila,0).toString());
            PreparedStatement ps;
            ResultSet rs;
            Connection con = Conexion.getConnection();
            ps= con.prepareStatement("SELECT  Pais, Region, Capitan, Ranking,Mundiales_Ganados FROM jugadores WHERE Id=?");

            ps.setInt(1,Id);
            rs = ps.executeQuery();

            while(rs.next()){
                txtid.setText(String.valueOf(Id));
                txtpais.setText(rs.getString("PAIS"));
                txtregion.setText(rs.getString("REGION"));
                txtcapitan.setText(rs.getString("CAPITAN"));
                txtranking.setText(rs.getString("RANKING"));
                txtmundiales.setText(rs.getString("MUNDIALES_GANADOS"));

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_tblmundialMouseClicked
