    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        String ID= txtid.getText();
        String PAIS= txtpais.getText();
        String REGION= txtregion.getText();
        String CAPITAN= txtcapitan.getText();
        String RANKING= txtranking.getText();
        String MUNDIALES= txtmundiales.getText();
        try {
            Connection con = Conexion.getConnection();
            PreparedStatement ps= con.prepareStatement("INSERT INTO jugadores(Pais, Region, Capitan, Ranking,Mundiales_Ganados) VALUES(?, ?, ?, ?,?)");

            ps.setString(1, PAIS);
            ps.setString(2, REGION);
            ps.setString(3, CAPITAN);
            ps.setString(4, RANKING);
            ps.setString(5,MUNDIALES );
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"SE GUARDO EL REGISTRO");
            limpiar();
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }

    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:

        int ID= Integer.parseInt(txtid.getText());
        String PAIS= txtpais.getText();
        String REGION= txtregion.getText();
        String CAPITAN= txtcapitan.getText();
        String RANKING= txtranking.getText();
        String MUNDIALES = txtmundiales.getText();
        try {
            Connection con = Conexion.getConnection();
            PreparedStatement ps= con.prepareStatement("UPDATE jugadores SET Pais=?, Region=?, Capitan=?, Ranking=?,Mundiales_Ganados=? WHERE Id = ?");

            ps.setString(1, PAIS);
            ps.setString(2, REGION);
            ps.setString(3, CAPITAN);
            ps.setString(4, RANKING);
            ps.setString(5, MUNDIALES);
            ps.setInt(6,ID);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null,"SE MODIFICO EL REGISTRO");
            limpiar();
            cargarTabla();

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

